import numpy as np

# Create a NumPy array
data = np.array([1, 2, 3, 4, 5])

# Perform basic operations
mean_value = np.mean(data)
sum_value = np.sum(data)
max_value = np.max(data)
min_value = np.min(data)

print("Mean:", mean_value)
print("Sum:", sum_value)
print("Max:", max_value)
print("Min:", min_value)